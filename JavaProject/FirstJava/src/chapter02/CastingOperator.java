package chapter02;

public class CastingOperator {

	public static void main(String[] args) {

		
		char c = 'a';
		System.out.println(c);
		
		
		int n1 = 1;
		
		System.out.println(c+n1);
		
		int n2 = 10;
		System.out.println(n2);
		
		float n3 = 10.0f;
		System.out.println(n2+n3); //int float 중에 float 연산자가 더 크니까 float로 변환되어 연산이 된다.
		
		
	}

}
